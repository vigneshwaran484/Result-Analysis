"""
tests/test_upload.py
Tests for the 4 upload endpoints using the FastAPI TestClient.
Uses an in-memory Excel file generated by pandas.
"""
import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

import io
import pandas as pd
import pytest
from fastapi.testclient import TestClient
from main import app

client = TestClient(app)


def _make_excel(df: pd.DataFrame) -> bytes:
    buf = io.BytesIO()
    df.to_excel(buf, index=False)
    buf.seek(0)
    return buf.read()


# ── Test data ─────────────────────────────────────────────────────────────────

STUDENTS_DATA = pd.DataFrame([
    {"register_no": "A001", "name": "Alice", "section": "A", "batch": "2021-25", "lateral_entry": "No"},
    {"register_no": "B001", "name": "Bob", "section": "B", "batch": "2021-25", "lateral_entry": "No"},
])

SUBJECTS_DATA = pd.DataFrame([
    {"subject_code": "CS01", "subject_name": "Maths", "semester": 8, "credits": 4,
     "staff_a": "Mr X", "staff_b": "Ms Y", "staff_c": "Dr Z"},
])

CURRENT_DATA = pd.DataFrame([
    {"register_no": "A001", "subject_code": "CS01", "grade": "O"},
    {"register_no": "B001", "subject_code": "CS01", "grade": "A+"},
])

HISTORICAL_DATA = pd.DataFrame([
    {"register_no": "A001", "semester": 7, "subject_code": "CS70", "grade_point": 8.0, "arrear": "No"},
])


# ── Upload tests ──────────────────────────────────────────────────────────────

def test_upload_students():
    resp = client.post(
        "/upload/students",
        files={"file": ("students.xlsx", _make_excel(STUDENTS_DATA), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")},
    )
    assert resp.status_code == 200
    data = resp.json()
    assert data["status"] == "ok"
    assert data["rows_loaded"] == 2


def test_upload_subjects():
    resp = client.post(
        "/upload/subjects",
        files={"file": ("subjects.xlsx", _make_excel(SUBJECTS_DATA), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")},
    )
    assert resp.status_code == 200
    assert resp.json()["rows_loaded"] == 1


def test_upload_current_results():
    resp = client.post(
        "/upload/current-results",
        files={"file": ("current.xlsx", _make_excel(CURRENT_DATA), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")},
    )
    assert resp.status_code == 200
    assert resp.json()["rows_loaded"] == 2


def test_upload_historical_results():
    resp = client.post(
        "/upload/historical-results",
        files={"file": ("hist.xlsx", _make_excel(HISTORICAL_DATA), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")},
    )
    assert resp.status_code == 200
    assert resp.json()["rows_loaded"] == 1


def test_status_after_all_uploads():
    # Re-upload all first (test isolation)
    for endpoint, df in [
        ("/upload/students", STUDENTS_DATA),
        ("/upload/subjects", SUBJECTS_DATA),
        ("/upload/current-results", CURRENT_DATA),
        ("/upload/historical-results", HISTORICAL_DATA),
    ]:
        client.post(endpoint, files={"file": ("f.xlsx", _make_excel(df), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")})

    resp = client.get("/upload/status")
    assert resp.status_code == 200
    data = resp.json()
    assert data["ready"] is True


def test_upload_invalid_file():
    resp = client.post(
        "/upload/students",
        files={"file": ("bad.xlsx", b"not an excel file", "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")},
    )
    assert resp.status_code == 422


def test_upload_missing_columns():
    bad_df = pd.DataFrame([{"wrong_col": "x"}])
    resp = client.post(
        "/upload/students",
        files={"file": ("bad.xlsx", _make_excel(bad_df), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")},
    )
    assert resp.status_code == 422
